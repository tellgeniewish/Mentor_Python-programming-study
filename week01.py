# 25. 사용자가 입력한 3개의 정수에 대해 아래의 조건을 만족하는 코드를 작성하시오.
# - 세 정수가 모두 짝수이면 "모두 짝수입니다."
# - 세 정수가 모두 홀수이면 "모두 홀수입니다."
# - 정수가 섞여 있으면 "홀수와 짝수가 섞여 있습니다."
# - 만약 한 개라도 0이 입력되면 "0은 포함될 수 없습니다."
a, b, c = map(int, input("세 정수를 입력하세요: ").split())

if a == 0 or b == 0 or c == 0:
    print("0은 포함될 수 없습니다.")
elif a % 2 == 0 and b % 2 == 0 and c % 2 == 0:
    print("모두 짝수입니다.")
elif a % 2 != 0 and b % 2 != 0 and c % 2 != 0:
    print("모두 홀수입니다.")
else:
    print("홀수와 짝수가 섞여 있습니다.")
# ```python
# 1 2 4
# 홀수와 짝수가 섞여 있습니다.
# ```

# ```python
# 27 0 3
# 0은 포함될 수 없습니다.
# ```

# ```python
# 100 70 32
# 모두 짝수입니다.
# ```

# ```python
# 7 11 77
# 모두 홀수입니다.
# ```